<h2>Política de Privacidade — Mu Reborn</h2>

<p>O Mu Reborn, na qualidade de controlador de dados, valoriza a sua privacidade e atua em conformidade com as legislações aplicáveis de proteção de dados pessoais, incluindo a Lei Geral de Proteção de Dados (LGPD) e demais normas pertinentes.</p>

<p>Esta Política de Privacidade explica de forma clara como coletamos, utilizamos, armazenamos, compartilhamos e protegemos seus dados pessoais, bem como os seus direitos enquanto titular desses dados.</p>

<p>Ao utilizar nossos serviços, você declara estar ciente e concorda com o tratamento de seus dados pessoais conforme descrito nesta política.</p>

<p>Esta política se aplica sempre que você:</p>

<ul>
	<li>jogar no servidor Mu Reborn;</li>
	<li>acessar nossos serviços em plataformas oficiais;</li>
	<li>utilizar nosso site ou serviços relacionados;</li>
	<li>participar de comunidades oficiais do servidor.</li>
</ul>

<p>Todos esses serviços são denominados em conjunto como "Serviços".</p>

<h3>1. Dados Pessoais Coletados</h3>

<p>Ao utilizar nossos Serviços, podemos coletar, utilizar, armazenar e transferir diferentes tipos de dados pessoais. Como regra geral, não coletamos dados sensíveis, como informações sobre saúde, religião, opiniões políticas ou orientação sexual.</p>

<p>Para criar uma conta e acessar determinados recursos, você deve possuir a idade mínima exigida pela legislação aplicável em sua jurisdição para consentir com o tratamento de dados pessoais.</p>

<h4>1.1 Informações Fornecidas pelo Usuário</h4>

<h5>Dados de Conta</h5>

<p>Para acessar o Mu Reborn, é necessário criar uma conta. Podemos coletar:</p>

<ul>
	<li>endereço de e-mail;</li>
	<li>nome completo ou nome de usuário;</li>
	<li>senha criptografada;</li>
	<li>data de nascimento;</li>
	<li>informações de perfil (avatar, preferências e configurações).</li>
</ul>

<h5>Configuração e Gerenciamento da Conta</h5>

<p>Coletamos dados relacionados a:</p>

<ul>
	<li>criação e configuração da conta;</li>
	<li>recuperação de acesso;</li>
	<li>exclusão ou alteração de conta.</li>
</ul>

<h5>Verificação e Conformidade</h5>

<p>Podemos coletar informações necessárias para verificar sua elegibilidade e garantir o cumprimento de nossos termos de uso.</p>

<h5>Suporte e Atendimento</h5>

<p>Quando você entra em contato com nosso suporte, podemos registrar:</p>

<ul>
	<li>detalhes da solicitação;</li>
	<li>informações da conta;</li>
	<li>dados relacionados aos personagens ou progresso no jogo.</li>
</ul>

<h5>Compras e Transações</h5>

<p>Ao realizar compras dentro do jogo (itens virtuais, assinaturas ou benefícios), coletamos informações da transação.</p>
<p>Não armazenamos dados de cartão de crédito, pois o processamento é realizado por provedores de pagamento terceirizados.</p>

<h5>Pesquisas e Eventos</h5>

<p>Se você participar de eventos promocionais ou pesquisas, poderemos coletar dados adicionais como:</p>

<ul>
	<li>informações de contato;</li>
	<li>preferências;</li>
	<li>feedback.</li>
</ul>

<h4>1.2 Informações Coletadas Automaticamente</h4>

<p>Durante o uso do servidor e serviços do Mu Reborn, determinadas informações são coletadas automaticamente.</p>

<h5>Dados de Identificação Técnica</h5>

<ul>
	<li>endereço IP;</li>
	<li>identificadores de conta;</li>
	<li>informações do dispositivo;</li>
	<li>sistema operacional e plataforma;</li>
	<li>identificadores de publicidade ou dispositivo.</li>
</ul>

<h5>Interações com Suporte</h5>

<p>Registros de comunicação com nossa equipe para melhoria do atendimento.</p>

<h5>Comunicações Entre Usuários</h5>

<p>Mensagens enviadas por meio de sistemas de chat dentro do jogo podem ser registradas para segurança, moderação e cumprimento das regras do servidor.</p>

<h3>2. Compartilhamento de Dados Pessoais</h3>

<p>Seus dados poderão ser compartilhados apenas quando necessário e nas seguintes situações:</p>

<h4>Provedores de Serviços</h4>

<p>Empresas terceiras que auxiliam na operação do servidor, como:</p>

<ul>
	<li>hospedagem e infraestrutura em nuvem;</li>
	<li>sistemas de pagamento;</li>
	<li>serviços de comunicação;</li>
	<li>sistemas de segurança.</li>
</ul>

<h4>Parceiros Operacionais</h4>

<p>Plataformas ou serviços envolvidos na distribuição ou funcionamento do jogo.</p>

<h4>Obrigação Legal</h4>

<p>Quando exigido por lei, ordem judicial ou autoridades competentes.</p>

<h4>Proteção e Segurança</h4>

<p>Para proteger os usuários, prevenir fraudes, aplicar regras do servidor ou garantir a integridade do serviço.</p>

<h3>3. Segurança das Informações</h3>

<p>O Mu Reborn adota medidas técnicas e organizacionais adequadas para proteger seus dados pessoais, incluindo:</p>

<ul>
	<li>criptografia de dados;</li>
	<li>controle de acesso restrito;</li>
	<li>monitoramento de segurança;</li>
	<li>proteção contra acesso não autorizado, alteração ou divulgação indevida.</li>
</ul>

<p>Apesar disso, nenhum sistema é totalmente seguro, e não podemos garantir segurança absoluta.</p>

<h3>4. Retenção de Dados</h3>

<p>Seus dados pessoais serão armazenados apenas pelo tempo necessário para:</p>

<ul>
	<li>fornecer os Serviços;</li>
	<li>cumprir obrigações legais;</li>
	<li>resolver disputas;</li>
	<li>fazer cumprir nossos termos e políticas.</li>
</ul>

<p>Após esse período, os dados poderão ser excluídos ou anonimizados.</p>

<h3>5. Direitos do Titular dos Dados</h3>

<p>Você possui os seguintes direitos em relação aos seus dados pessoais:</p>

<h4>Acesso</h4>
<p>Solicitar informações sobre os dados que mantemos sobre você.</p>

<h4>Correção</h4>
<p>Atualizar ou corrigir dados incompletos ou incorretos.</p>

<h4>Portabilidade</h4>
<p>Receber seus dados em formato estruturado.</p>

<h4>Exclusão</h4>
<p>Solicitar a remoção de seus dados pessoais, quando legalmente aplicável.</p>

<h4>Restrição de Tratamento</h4>
<p>Limitar o uso de seus dados em determinadas circunstâncias.</p>

<h4>Revogação do Consentimento</h4>
<p>Retirar o consentimento previamente concedido.</p>

<p>Para exercer seus direitos, entre em contato com nossa equipe de suporte.</p>

<h3>6. Uso do Discord</h3>

<p>O Mu Reborn utiliza o Discord como canal oficial de comunicação entre jogadores e administradores.</p>
<p>O tratamento de dados realizado pelo Discord segue suas próprias políticas de privacidade, sendo de responsabilidade da plataforma.</p>

<h3>7. Alterações nesta Política</h3>

<p>O Mu Reborn poderá atualizar esta Política de Privacidade periodicamente para refletir mudanças legais, técnicas ou operacionais.</p>
<p>As alterações serão comunicadas por meio do site oficial, launcher ou e-mail quando aplicável.</p>

<p>Recomendamos a revisão periódica deste documento.</p>
